package org.esupportail.smsuapimail.exceptions;

public class SmsSenderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7216399876981057544L;

	public SmsSenderException() {
		super();
	}

	public SmsSenderException(final String arg0) {
		super(arg0);
		
	}

	public SmsSenderException(final Throwable arg0) {
		super(arg0);
		
	}

	public SmsSenderException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

}
